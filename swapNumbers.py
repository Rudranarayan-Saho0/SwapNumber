

#Swap Two Variables

# User Input: Input Two Variables
A = input("Enter First Number: ")
B = input("Enter Second Number: ")

print(f"Before Swapping: A = {A}, B = {B}") # Print Original Value

# Swap The Values
A, B = B, A

print(f"After Swapping: A = {A}, B = {B}") # Display The Swapped Values
